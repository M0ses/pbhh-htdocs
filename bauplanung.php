<div class="jumbotron">
<strong>Bauplanung aus einer Hand.</strong><br>
In Sachen Bauplanung liegt ein wesentlicher Vorteil in der "Planung aus einer Hand":
von der Bauplanung, über die Statik bis hin zur Energieberatung. Dies spart für den Bauherrn Kosten und Zeit. Zudem werden die kompletten Bauprojekte auf modernsten CAD-Programmen als 3D-Konstruktion gezeichnet, d.h. der Kunde kann sich sein neues Eigenheim schon in der Planungsphase dreidimensional ansehen.
